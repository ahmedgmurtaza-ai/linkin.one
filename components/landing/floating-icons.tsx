export function FloatingIcons() {
  const canvasRef = useRef<HTMLDivElement>(null)
  const iconsRef = useRef<FloatingIcon[]>([])
  const animationFrameRef = useRef<number | null>(null)

  useEffect(() => {
    if (!canvasRef.current) return

    const container = canvasRef.current
    const rect = container.getBoundingClientRect()

    // Initialize floating icons with random positions
    iconsRef.current = platformIcons.map((icon, index) => ({
      id: `icon-${index}`,
      icon,
      x: Math.random() * rect.width,
      y: Math.random() * rect.height,
      speed: 0.3 + Math.random() * 0.5,
      angle: Math.random() * Math.PI * 2,
    }))

    const animate = () => {
      iconsRef.current = iconsRef.current.map((item) => {
        let newX = item.x + Math.cos(item.angle) * item.speed
        let newY = item.y + Math.sin(item.angle) * item.speed
        let newAngle = item.angle

        // Bounce off edges
        if (newX < 0 || newX > rect.width) {
          newAngle = Math.PI - newAngle
          newX = Math.max(0, Math.min(rect.width, newX))
        }
        if (newY < 0 || newY > rect.height) {
          newAngle = -newAngle
          newY = Math.max(0, Math.min(rect.height, newY))
        }

        return {
          ...item,
          x: newX,
          y: newY,
          angle: newAngle,
        }
      })

      // Update DOM
      const elements = container.querySelectorAll('[data-floating-icon]')
      elements.forEach((el, index) => {
        const icon = iconsRef.current[index]
        if (icon && el instanceof HTMLElement) {
          el.style.transform = `translate(${icon.x}px, ${icon.y}px)`
        }
      })

      animationFrameRef.current = requestAnimationFrame(animate)
    }

    animate()

    return () => {
      if (animationFrameRef.current) {
        cancelAnimationFrame(animationFrameRef.current)
      }
    }
  }, [])

  return (
    <div ref={canvasRef} className="absolute inset-0 overflow-hidden pointer-events-none">
      {iconsRef.current.map((item, index) => (
        <div
          key={item.id}
          data-floating-icon
          className="absolute transition-transform duration-75 ease-linear opacity-40 hover:opacity-80"
          style={{
            transform: `translate(${item.x}px, ${item.y}px)`,
          }}
        >
          {platformIcons[index]}
        </div>
      ))}
    </div>
  )
}
